import { createHash } from "node:crypto";
import { readFile, writeFile } from "node:fs/promises";

function stableStringify(value) {
  if (Array.isArray(value)) return `[${value.map(stableStringify).join(",")}]`;
  if (value && typeof value === "object") {
    return `{${Object.keys(value).sort().map((k) => `${JSON.stringify(k)}:${stableStringify(value[k])}`).join(",")}}`;
  }
  return JSON.stringify(value);
}

const source = JSON.parse(await readFile("data/static-db-source.json", "utf8"));
const { species, moves, abilities, learnsets } = source;

const hashes = {
  species: createHash("sha256").update(stableStringify(species)).digest("hex"),
  moves: createHash("sha256").update(stableStringify(moves)).digest("hex"),
  abilities: createHash("sha256").update(stableStringify(abilities)).digest("hex"),
  learnsets: createHash("sha256").update(stableStringify(learnsets)).digest("hex"),
};

const out = `/* AUTO-GENERATED by scripts/generate-static-db.mjs. Do not edit manually. */\n\nexport const STATIC_DB = ${JSON.stringify({ species, moves, abilities, learnsets }, null, 2)};\n\nexport const STATIC_DB_SUMMARY = ${JSON.stringify({
  counts: {
    species: species.length,
    moves: moves.length,
    abilities: abilities.length,
    learnsets: Object.keys(learnsets).length,
  },
  hashes,
}, null, 2)};\n\nexport const SPECIES = STATIC_DB.species;\nexport const MOVES = STATIC_DB.moves;\nexport const ABILITIES = STATIC_DB.abilities;\nexport const LEARNSETS = STATIC_DB.learnsets;\n\nexport const SPECIES_BY_ID = Object.fromEntries(SPECIES.map((s) => [s.id, s]));\nexport const MOVES_BY_ID = Object.fromEntries(MOVES.map((m) => [m.id, m]));\nexport const ABILITIES_BY_ID = Object.fromEntries(ABILITIES.map((a) => [a.id, a]));\n`;

await writeFile("src/shared/staticDb.generated.js", out);
console.log("Generated src/shared/staticDb.generated.js");
